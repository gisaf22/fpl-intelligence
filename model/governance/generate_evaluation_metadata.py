"""Generate evaluation_metadata.yaml from per-lens verdict records + synth decisions.

ADR-009 Phase C. ``evaluation_metadata.yaml`` is the scoring decision-of-record; it
must be machine-generated, not hand-authored (§2). This generator is the writer:

  per-lens evidence.yaml + annotations.yaml   (research/families/*/validate/)
  + model/assemble/synth01_decisions.yaml
        ── merge ──▶  model/governance/evaluation_metadata.yaml

It reads every input **file-based** (no import of ``research`` or ``model.assemble``),
mirroring how ``model/governance/promote.py`` reads ``research/findings/`` — so the
``research ↛ model`` / ``no_model_to_research_analysis`` contracts stay green. Finding
keys are built via the ADR-003 codec (``domain.registry.finding_key.build_key``).

The two evaluation tiers stay separate by design: foundation-registry verdicts are not
touched here; this generator only assembles the lens/synth tier the scorer reads via
``domain.registry.governance_lookup.get_signal_governance``.
"""

from __future__ import annotations

import argparse
from pathlib import Path
from typing import Any

import yaml

from domain.registry.finding_key import build_key
from model.governance.verdict_records import (
    EVIDENCE_FIELDS,
    LENS_VALIDATE_DIRS,
    LensVerdict,
    load_all_lens_verdicts,
)

OUT_PATH = Path("model/governance/evaluation_metadata.yaml")
SYNTH_DECISIONS_PATH = Path("model/assemble/synth01_decisions.yaml")

# Per-position field order, matching the historical hand-authored layout.
_ANNOTATION_ORDER = (
    "lifecycle_state",
    "downstream_status",
    "leakage_risk",
    "behavioral_reason",
    "source_gate_decisions",
)
_SYNTH_ORDER = ("synth01_decision", "composition_weight", "composition_role")

# Governance schema constants — the controlled vocabularies and gate config carried
# at the top of evaluation_metadata.yaml. These belong to governance, not to a study.
_TOP_LEVEL: dict[str, Any] = {
    "gate_criteria_ref": "docs/governance/evaluation-gate-criteria.md",
    "block_stability_total": 3,
    "decision_class_vocab": {
        "informative": "Passes Gates 1-3 at this position; advances to synthesis candidate",
        "uninformative": "Fails Gate 1 (CI crosses zero) OR Gate 2 (fails decision relevance) OR Gate 3 (< 2/3 blocks)",
        "conditional": "Passes gates but with documented scope constraint (naive baseline only; "
        "or position-specific caveat)",
        "excluded": "Position not in scope by ontological design (e.g. GK for xgi; FWD/GKP for fixture_count)",
    },
    "lifecycle_state_vocab": {
        "candidate": "Signal-position pair approved for SYNTH-01 entry",
        "excluded": "Not advancing to synthesis",
        "not_applicable": "Position not studied; excluded by design before lens study ran",
        "approved": "Signal-position pair approved by SYNTH-01 partial rho evaluation. "
        "All G-SYNTH1-* decisions recorded in model/assemble/synth01_decisions.yaml.",
    },
    "downstream_status_vocab": {
        "eligible": "Approved for SYNTH-01; no blocking caveats",
        "caveated": "Approved for SYNTH-01 but with a documented constraint "
        "(borderline stability, naive baseline concern)",
        "blocked": "Not eligible for synthesis or scoring",
        "approved": "SYNTH-01 APPROVED-* decision. Signal may enter operational composition. "
        "Weight in model/assemble/synth01_decisions.yaml.",
    },
    "leakage_risk_vocab": {
        "none": "No leakage concern",
        "evaluation_circularity": "Signal is a lagged/rolling version of the evaluation target",
        "direct": "Signal is a computational component of the scoring target (bonus, bps)",
    },
}
_VERSION = "3.0"
_VERSION_NOTE = (
    "v3.0 generated by model/governance/generate_evaluation_metadata.py from per-lens "
    "verdict records (research/families/*/validate/{evidence,annotations}.yaml) merged "
    "with model/assemble/synth01_decisions.yaml (ADR-009 Phase C)"
)


def _load_synth_decisions(path: Path) -> dict[str, dict[str, Any]]:
    """Return synth decisions keyed by their composite finding key (ADR-003)."""
    with path.open() as fh:
        data = yaml.safe_load(fh)
    return {d["key"]: d for d in data["decisions"]}


def _per_position(
    signal: str,
    position: str,
    lens: LensVerdict,
    synth_by_key: dict[str, dict[str, Any]],
) -> dict[str, Any]:
    """Assemble one signal-position record from evidence + annotation + synth merge."""
    ann = lens.annotations[signal][position]
    ev = lens.evidence.get(signal, {}).get(position)

    record: dict[str, Any] = {}
    # Evidence tier (null for design-excluded positions that were never studied).
    for field in EVIDENCE_FIELDS:
        if field == "decision_class":
            continue
        record[field] = ev[field] if ev else None
    # decision_class: annotation override wins (design exclusions / documented caveats),
    # else the study-computed class.
    if "decision_class" in ann:
        record["decision_class"] = ann["decision_class"]
    else:
        record["decision_class"] = ev["decision_class"] if ev else None

    # Judgment tier.
    for field in _ANNOTATION_ORDER:
        record[field] = ann[field]
    if "notes" in ann:
        record["notes"] = ann["notes"]

    # Synth tier — defaults, then overlay the matching synth decision (automated merge).
    for field in _SYNTH_ORDER:
        record[field] = None
    synth_key = build_key(signal, lens.lens, lens.target, position)
    decision = synth_by_key.get(synth_key)
    if decision is not None:
        record["synth01_decision"] = decision["decision"]
        record["composition_role"] = decision["contribution_class"]
        # EXCLUDED-REDUNDANT carries no operational weight (recorded null, not 0.0).
        record["composition_weight"] = (
            None if decision["decision"] == "EXCLUDED-REDUNDANT" else decision["composition_weight"]
        )
        record["synth01_decision_id"] = decision["key"]
        if decision["decision"].startswith("APPROVED") and record["lifecycle_state"] != "approved":
            raise ValueError(
                f"Synth APPROVED decision {synth_key!r} but annotation lifecycle_state="
                f"{record['lifecycle_state']!r}; verdict records are inconsistent."
            )
    return record


def build_findings(lenses: list[LensVerdict], synth_by_key: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    """Build the evaluation_findings list from loaded lens verdicts + synth decisions."""
    findings: list[dict[str, Any]] = []
    for lens in lenses:
        for signal, positions in lens.annotations.items():
            findings.append(
                {
                    "key": build_key(signal, lens.lens, lens.target),
                    "signal": signal,
                    "lens": lens.lens,
                    "target": lens.target,
                    "per_position": {pos: _per_position(signal, pos, lens, synth_by_key) for pos in positions},
                }
            )
    return findings


def generate(
    lens_dirs: dict[str, Path] | None = None,
    synth_path: Path = SYNTH_DECISIONS_PATH,
) -> dict[str, Any]:
    """Produce the full evaluation_metadata mapping (does not write to disk)."""
    lenses = load_all_lens_verdicts(lens_dirs)
    synth_by_key = _load_synth_decisions(synth_path)
    return {
        **_TOP_LEVEL,
        "evaluation_findings": build_findings(lenses, synth_by_key),
        "version": _VERSION,
        "version_note": _VERSION_NOTE,
    }


_BANNER = (
    "# GENERATED — DO NOT EDIT BY HAND (ADR-009 Phase C).\n"
    "# Regenerate with: python -m model.governance.generate_evaluation_metadata\n"
    "# Source: research/families/*/validate/{evidence,annotations}.yaml\n"
    "#       + model/assemble/synth01_decisions.yaml\n"
)


def write(out_path: Path = OUT_PATH, **kwargs: Any) -> Path:
    """Generate and write evaluation_metadata.yaml (with a generated-file banner)."""
    payload = generate(**kwargs)
    with out_path.open("w") as fh:
        fh.write(_BANNER)
        yaml.dump(payload, fh, default_flow_style=False, sort_keys=False, allow_unicode=True)
    return out_path


def main(argv: list[str] | None = None) -> int:
    """CLI entry point."""
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--out", type=Path, default=OUT_PATH, help=f"Output path (default {OUT_PATH}).")
    parser.add_argument("--synth", type=Path, default=SYNTH_DECISIONS_PATH, help="synth01_decisions.yaml path.")
    args = parser.parse_args(argv)
    out = write(out_path=args.out, synth_path=args.synth)
    n = sum(len(e["per_position"]) for e in generate(synth_path=args.synth)["evaluation_findings"])
    print(f"Wrote {out} ({n} signal-position records from {len(LENS_VALIDATE_DIRS)} lenses)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
